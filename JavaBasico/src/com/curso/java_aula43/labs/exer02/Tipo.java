package com.curso.java_aula43.labs.exer02;

public class Tipo extends Contribuinte {
	
	private String f;
	private String j;
	private boolean tipo = true;
	
	public String getF() {
		return f;
	}
	public void setF(String f) {
		this.f = f;
	}
	public String getJ() {
		return j;
	}
	public void setJ(String j) {
		this.j = j;
	}
	
	public boolean getTipo() {
		return tipo;
	}
	public void setTipo(boolean tipo) {
		this.tipo = tipo;
	}
	
}
